#include "libft.h"

char	*ft_strstr(const char *haystack, const char *needle)
{
	size_t		len;

	if (!(len = ft_strlen(needle)))
		return ((char *)haystack);
	while (*haystack)
	{
		if (ft_strnequ(needle, haystack, len))
			return ((char *)haystack);
		++haystack;
	}
	return (NULL);
}
